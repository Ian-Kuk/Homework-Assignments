/**
 * Class that creates a tuple of the three values that make up the polynomial
 */
public class Tuple
{
    public final Integer a;
    public final Integer b;
    public final Integer c;

    /**
     * Constructor that makes the tuple out of the values
     * @param a first value of polynomial
     * @param b second value of polynomial
     * @param c third value of polynomial
     */
    public Tuple(Integer a, Integer b, Integer c)
    {
        this.a = a;
        this.b = b;
        this.c = c;
    }
}
